    private void updatePlayerPosition() {
        if (player.position >= boardSize) {
            switch (currentLevel) {
                case 1:
                    // حفظ سكور المستوى 1
                    ScoreFileManager.saveScore(player.name, player.score, currentLevel);
                    totalScore += player.score;
                    player.score = 0;
                    JOptionPane.showMessageDialog(this, "Level 1 Complete! Starting Level 2...");
                    startLevel(2, 30);  // عدد الخانات في المستوى الثاني
                    return;

                case 2:
                    // حفظ سكور المستوى 2
                    ScoreFileManager.saveScore(player.name, player.score, currentLevel);
                    totalScore += player.score;
                    player.score = 0;
                    JOptionPane.showMessageDialog(this, "Level 2 Complete! Starting Level 3...");
                    startLevel(3, 30);  // عدد الخانات في المستوى الثالث
                    return;

                case 3:
                    // حفظ سكور المستوى 3 وعرض النتيجة النهائية
                    ScoreFileManager.saveScore(player.name, player.score, currentLevel);
                    totalScore += player.score;
                    JOptionPane.showMessageDialog(this, "🎮 Game Over!\nYour Final Total Score: " + totalScore);
                    this.setVisible(false);
                    new MainMenu().setVisible(true);
                    return;
            }
        }

        Node currentNode = map.getNodeAt(player.position);
        if (currentNode == null) {
            return;
        }

        String type = currentNode.type;

        switch (type) {
            case "Treasure":
                player.score += 10;
                break;
            case "Trap":
                player.score -= 5;
                break;
            case "Bomb":
                // 🔊 Play explosion sound
                playSound("saidgame/sounds/explosion-312361.wav");

                // 💥 Flash board red and black
                for (int flash = 0; flash < 4; flash++) {
                    for (int i = 0; i < boardButtons.length; i++) {
                        if (boardButtons[i] != null) {
                            boardButtons[i].setBackground(flash % 2 == 0 ? Color.RED : Color.BLACK);
                        }
                    }
                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }

                // ❌ Reset score
                player.score = 0;

                // 🎯 Show ❌ on the bomb tile
                if (player.position > 0 && player.position - 1 < boardButtons.length) {
                    JButton bombTile = boardButtons[player.position - 1];
                    bombTile.setBackground(Color.BLACK);
                    bombTile.setForeground(Color.WHITE);
                    bombTile.setFont(new Font("Arial", Font.BOLD, 20));
                    bombTile.setText("❌");
                }

                JOptionPane.showMessageDialog(this, "💣 Boom! You stepped on a bomb. Your score is now 0!");
                fillBoard();
                break;

            case "Earthquake":
                // ✅ تشغيل الصوت
                playSound("saidgame/sounds/quake-and-break-99034.wav");

                // ✨ وميض الأزرار
                for (int flash = 0; flash < 4; flash++) {
                    for (int i = 0; i < boardButtons.length; i++) {
                        if (boardButtons[i] != null) {
                            boardButtons[i].setBackground(flash % 2 == 0 ? Color.DARK_GRAY : Color.LIGHT_GRAY);
                        }
                    }
                    try {
                        Thread.sleep(150);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }

                shuffleMap();
                JOptionPane.showMessageDialog(this, "🌋 Earthquake! The map has been shuffled.");
                fillBoard();
                break;

        }

        labelInfo.setText("Player Position: " + player.position + " | Score: " + player.score);

        if (boardButtons == null) {
            return;
        }

        for (int i = 0; i < boardButtons.length; i++) {
            JButton btn = boardButtons[i];
            if (btn == null) {
                continue;
            }
            if (i + 1 > boardSize) {
                continue;
            }

            Node node = map.getNodeAt(i + 1);
            btn.setFont(new Font("Arial", Font.PLAIN, 12));

            if (node != null) {
                Color bg;
                switch (node.type) {
                    case "Treasure":
                        bg = Color.YELLOW;
                        break;
                    case "DoubleTreasure":
                        bg = Color.MAGENTA;
                        break;
                    case "Trap":
                        bg = Color.RED;
                        break;
                    case "Earthquake":
                        bg = Color.GRAY;
                        break;
                    case "Whirlpool":
                        bg = Color.BLUE;
                        break;
                    case "Bomb":
                        bg = new Color(139, 0, 0);
                        break;
                    default:
                        if (node.type.startsWith("Forward")) {
                            bg = Color.CYAN;
                        } else if (node.type.startsWith("Backward")) {
                            bg = Color.ORANGE;
                        } else {
                            bg = Color.WHITE;
                        }
                        break;
                }

                btn.setBackground(bg);
                btn.setText("<html>" + node.id + "<br>" + node.type + "</html>");
            }

            if (player.position > 0 && i == player.position - 1) {
                btn.setBackground(Color.GREEN);
                btn.setFont(new Font("Arial", Font.BOLD, 18));
                btn.setText("<html>" + (i + 1) + "<br>P</html>");
            }
        }
    }

    private void shuffleMap() {
        java.util.List<Node> nodes = new java.util.ArrayList<>();
        Node current = map.getHead();

        while (current != null) {
            if (!current.type.equals("Start")) {
                nodes.add(current);
            }
            current = current.next;
        }

        java.util.Collections.shuffle(nodes);

        Node startNode = map.getHead();
        Node temp = startNode;

        for (Node node : nodes) {
            temp.next = node;
            temp = node;
        }

        temp.next = null;
    }
    
    
    public Node moveBackward(Node currentNode) {
    if (currentNode != null && currentNode.prev != null) {
        return currentNode.prev;
    }
    return currentNode;
}

    private void applySpotEffect() {
        if (player.position <= boardSize) {
            Node current = map.getNodeAt(player.position);
            if (current != null) {
                switch (current.type) {
                    case "Treasure":
                        player.score += 10;
                        break;
                    case "DoubleTreasure":
                        player.score += 20;
                        break;
                    case "Trap":
                        player.score -= 5;
                        break;
                    case "Earthquake":
                        player.position = new Random().nextInt(boardSize - 1) + 1;
                        break;
                    case "Whirlpool":
                        JOptionPane.showMessageDialog(this, "You landed on a Whirlpool! Skip one turn.");
                        return;
                    default:
                        if (current.type.startsWith("Forward")) {
                            int steps = Integer.parseInt(current.type.split(" ")[1]);
                            player.move(steps, boardSize);
                        } else if (current.type.startsWith("Backward")) {
                            int steps = Integer.parseInt(current.type.split(" ")[1]);
                            player.move(-steps, boardSize);
                        }
                        break;
                }
            }
        }  
    }

    private void startLevel(int level, int size) {   // انشا
        this.currentLevel = level;
        this.boardSize = size;
        map = new LinkedListMap(boardSize, currentLevel);
        player.position = 0;
        fillBoard();
        updatePlayerPosition();
    }

    private void playSound(String relativePath) {
        try {
            // Load the audio file as a resource from the classpath
            InputStream audioSrc = getClass().getClassLoader().getResourceAsStream(relativePath);
            if (audioSrc == null) {
                System.err.println("❌ Audio file not found: " + relativePath);
                return;
            }

            // Wrap the stream to make it readable for the audio system
            InputStream bufferedIn = new BufferedInputStream(audioSrc);
            AudioInputStream audioIn = AudioSystem.getAudioInputStream(bufferedIn);

            // Load the clip and play it
            Clip clip = AudioSystem.getClip();
            clip.open(audioIn);
            clip.start();
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
        
       
    }

