    private void initGame() {
        String name = JOptionPane.showInputDialog(this, "Enter your name:");
        player = new Player(name);
        player.position = 0;
        player.score = 0;
        labelInfo.setText("Player Position: 0 | Score: 0");
        map = new LinkedListMap(boardSize, currentLevel);
        boardButtons = new JButton[map.getCount()];  // ✅ التعديل هنا
        fillBoard();
        updatePlayerPosition();
    }

    private void fillBoard() {
        panelBoard.removeAll();

        // ✅ نحسب عدد الخانات فعليًا من الخريطة
        int count = 0;
        Node temp = map.getHead();
        while (temp != null) {
            count++;
            temp = temp.next;
        }

        boardButtons = new JButton[count];  // عدد الخانات الفعلي

        Node current = map.getHead();
        int i = 0;

        while (current != null) {
            JButton btn = new JButton(current.type);
            btn.setFont(new Font("Arial", Font.BOLD, 12));
            btn.setOpaque(true);
            btn.setBorderPainted(false);
            btn.setEnabled(false);

            switch (current.type) {
                case "Start":
                    btn.setBackground(Color.GREEN);
                    break;
                case "Treasure":
                    btn.setBackground(Color.YELLOW);
                    break;
                case "Trap":
                    btn.setBackground(Color.RED);
                    break;
                case "Empty":
                case "Normal":
                    btn.setBackground(Color.LIGHT_GRAY);
                    break;
                case "Earthquake":
                    btn.setBackground(new Color(96, 96, 96)); // رمادي غامق
                    break;
                case "Bomb":
                    btn.setBackground(new Color(139, 0, 0)); // أحمر داكن
                    break;
                default:
                    btn.setBackground(Color.WHITE);
                    break;
            }

            boardButtons[i] = btn;
            panelBoard.add(btn);
            current = current.next;
            i++;
        }

        panelBoard.revalidate();
        panelBoard.repaint();
    }


